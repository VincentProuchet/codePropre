/**Classe Représentant 
 * des calcul 
 * 
 * @author Vincent
 *
 */
public class Calcul {

	
	/** additionne les paramètres
	 * @param a
	 * @param b
	 * @return la somme des paramètres 
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/** soustrait le second paramètre du premier 
	 * @param a
	 * @param b
	 * @return la soustraction a-b
	 *  */
	public int soustraction(int a, int b){
		return a-b;
	}
}
